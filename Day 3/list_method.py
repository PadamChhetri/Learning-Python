list=[2,1,3]
list.append(4) #add one element 4 at the end
print(list)

list.sort() #Sorts in ascending orders
print(list)

list.sort(reverse=True) #Sorts in descending orders
print(list)

list.reverse() #sorts list in reverse
print(list)

list.insert(1,5) #insert new element at 1 index
print(list)

list.remove(1) #remove the first occurance of the element
print(list)

list.pop(0) #remove element at index
print(list)


